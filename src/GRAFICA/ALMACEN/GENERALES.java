    public void crearTabla() {
        columnModel = jTable1.getColumnModel();
        jTable1.setModel(dftm);
        dftm.setColumnIdentifiers(new Object[]{"Material","Tipo","Precio"});
           
    }
    public void llenartabla() {
        borrarTabla();
        lista=modAlma.ObtenerGenerales();
        for (material mat:lista) {
            dftm.addRow(new Object[]{mat.getMaterial(),mat.getTipo(),for4.format(mat.getPrecioUnidad())});
        }
    }

    public void borrarTabla() {
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            dftm.removeRow(i);
            i--;
        }
    }

    public void modificar(String nombre) {
        try {
            String precio = null;
            do {
                precio = JOptionPane.showInputDialog("precio de " + nombre);
            } while (!AUXILIAR.isFloat(precio) && Float.valueOf(precio) < 0);
            if (precio != null) {
                modAlma.guardarGeneral(nombre, Float.valueOf(precio));
                llenartabla();
            }

        } catch (Exception e) {
        }
    }

