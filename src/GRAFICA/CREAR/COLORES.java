    public void prepararTabla() {
        jTable1.setModel(dftm);
        columnModel = jTable1.getColumnModel();
        dftm.setColumnIdentifiers(new Object[]{"Nombre", "Fondo", "Laca", "Mancha", 
            "Tono","Chapacinta"});
    }

    public static void llenarTabla() {
        borrarTabla();
        lista = modMuebles.obtenerEstilos();
        for (ESTILOS es : lista) {
            dftm.addRow(new Object[]{es.getNombre(), es.getFondo(), es.getLaca(), 
                es.getMancha(), es.getTono(),es.getChapa()});
        }
    }

    public static void borrarTabla() {
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            dftm.removeRow(i);
            i--;
        }
    }

