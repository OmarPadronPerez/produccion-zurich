    public void borrarTabla(){
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            dftm.removeRow(i);
            i--;
        }
    }
    public void llenarTabla(){
        borrarTabla();
        for (PEDIDO com : lista) {//"Componente", "Modelo", "Color", "Tapiz","Falla"});
            dftm.addRow(new Object[]{com.getCantidad(),com.getComponente(),com.getModelo(),com.getColor(),
                com.getTapizado(),""
            });
            txtFolio.setText(com.getFolio());
            txtCliente.setText(com.getCliente());
            txtFechaDev.setText(fSalida.format(com.getFechaCreacion()) );
            txtFechaEnvio.setText(fSalida.format(com.getFechaEntrega()));
        }
    }
    public void recojerLista(){
        for(int i=0;i<jTable1.getRowCount();i++){
            lista.get(i).setObservaciones_pedido(jTable1.getValueAt(i, 5).toString());
        }
    }
    
    public void crearTabla(){
        columnModel = jTable1.getColumnModel();
        jTable1.setModel(dftm);
        dftm.setColumnIdentifiers(new Object[]{"cant","Componente", "Modelo", "Color", "Tapiz","Notas"});
    }
    
